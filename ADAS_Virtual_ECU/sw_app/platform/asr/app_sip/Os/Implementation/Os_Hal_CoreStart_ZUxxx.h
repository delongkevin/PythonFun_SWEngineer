{
  /* #10 set the corresponding core reset bit to zero */
  OS_HAL_RST_LPD_TOP &= ~(0x1uL << Core->CoreId);                                                                       /* PRQA S 0303 */ /* MD_Os_Hal_Rule11.4_0303 */ /* SBSW_OS_HAL_CORE_RESET_REGISTER */
}

