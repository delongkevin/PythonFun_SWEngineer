int32_t SDL_ESM_DCC_applicationCallbackFunction(SDL_ESM_Inst esmInst, SDL_ESM_IntType esmIntrType,
                                            uint32_t grpChannel,  uint32_t index, uint32_t intSrc, void *arg)
{
    /* DD-ID: {5CF4F492-3327-4453-AAA6-FADB407EBC18}*/
    Fusa_Application_ErrorLogForDID((uint8_t)e_errLog_DCC, (uint8_t)TRUE);
    int32_t retVal = SDL_PASS;
    (void)esmIntrType;
    (void)grpChannel;
    (void)arg;
    (void)index;

    switch (esmInst)
    {
    default:
    case SDL_ESM_INST_WKUP_ESM0:
        (void)SDL_ESM_disableIntr((uint32_t)SDL_WKUP_ESM0_CFG_BASE, intSrc);
        if (intSrc == SDLR_WKUP_ESM0_ESM_LVL_EVENT_GLUELOGIC_HFOSC0_CLKLOSS_GLUE_REF_CLK_LOSS_DETECT_OUT_0) 
        {
            Sdl_Application_SetClkDetErrorFlag();
        }
        break;
    case SDL_ESM_INST_MCU_ESM0:
        (void)SDL_ESM_disableIntr((uint32_t)SDL_MCU_ESM0_CFG_BASE, intSrc);
        if ((intSrc == SDLR_MCU_ESM0_ESM_LVL_EVENT_MCU_PLLFRACF2_SSMOD0_LOCKLOSS_IPCFG_0) || 
            (intSrc == SDLR_MCU_ESM0_ESM_LVL_EVENT_MCU_PLLFRACF2_SSMOD1_LOCKLOSS_IPCFG_0))
        {
            Sdl_Application_SetPllSlipMcuErrorFlag();
        }
        else if ((intSrc == SDLR_MCU_ESM0_ESM_LVL_EVENT_MCU_DCC0_INTR_ERR_LEVEL_0) ||
                 (intSrc == SDLR_MCU_ESM0_ESM_LVL_EVENT_MCU_DCC1_INTR_ERR_LEVEL_0))
        {
            Sdl_Application_SetMcuDCCErrorFlag();
        }
        else
        {
             /* Do nothing */
        }
        break;
    case SDL_ESM_INST_MAIN_ESM0:
        (void)SDL_ESM_disableIntr((uint32_t)SDL_ESM0_CFG_BASE, intSrc);
        if ((intSrc == SDLR_ESM0_ESM_LVL_EVENT_DCC4_INTR_ERR_LEVEL_0) ||
            (intSrc == SDLR_ESM0_ESM_LVL_EVENT_DCC6_INTR_ERR_LEVEL_0) ||
            (intSrc == SDLR_ESM0_ESM_LVL_EVENT_DCC8_INTR_ERR_LEVEL_0))
        {
            Sdl_Application_SetMainDCCErrorFlag();
        }
        else if (intSrc == SDLR_ESM0_ESM_LVL_EVENT_PLLFRACF2_SSMOD14_LOCKLOSS_IPCFG_0)
        {
            Sdl_Application_SetPllSlipMainErrorFlag();
        }
        else
        { 
            /* Do nothing */
        }
        break;
    }

#if defined (SDL_APP_DCC_UART_PRINTS_ENABLED)
    UART_printf("\nInterrupt is generated to ESM\n");
    UART_printf("    ESM Call back function called : instType 0x%x, intType 0x%x, " \
                "grpChannel 0x%x, index 0x%x, intSrc 0x%x \n",
                esmInst, esmIntrType, grpChannel, index, intSrc);
    UART_printf("    Take action \n\n");
#endif
    
    /* For clearing the interrupt */
    (void)SDL_ESM_clrNError(esmInst);
	
    return retVal;
}
