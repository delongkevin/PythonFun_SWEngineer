int32_t SDL_DCC_funcTest(void)
{
  /* DD-ID: {6939AEDC-0DB2-42b0-BEC1-7BE2A1949FE5}*/
    int32_t retVal, i, srcClk, testClk;
    uint32_t clk0Freq, clk1Freq, refClkRatioNum, testClkRatioNum;
    SDL_DCC_config configParams;
	SDL_DCC_staticRegs staticRegs;
	
	/* Steps
    1. Determine the clock frequencies for the sources
    2. Figure out the seed values for successful completion
    3. Configure Interrupts for normal completion
    4. Configure DCC instance
    5. Enable DCC
    6. Wait for normal completion
    7. Change reference clock source to higher frequency source (to simulate
        clock change)
    8. Configure DCC and Enable
    9. Wait for error notification
    */
     (void)memset((void*)&configParams, 0, sizeof(configParams));
#if defined (SDL_APP_DCC_UART_PRINTS_ENABLED)
    SDL_DCCAppPrint("\r\n" APP_DCC_STR ": Start\r\n");
	
    UART_printf("\nINSTANCE: DCC0\n");
#endif
	
	/* This loop cycling for 2 clock sources to achieve qualification testing.
		case 0:  Test clock   = MAIN PLLCTRL (MAIN_SYSCLK0)
		         Source clock = HFOSC0
		case 1:  Test clock   = MAIN PLLCTRL (MAIN_SYSCLK0)
		         Source clock = HFOSC1
	*/
	for(i=0; i<2; i++)
	{
		switch(i)
		{
			case 0:
					srcClk = SDL_DCC_CLK0_SRC_CLOCK0_0;
					testClk = SDL_DCC_CLK1_SRC_CLOCKSRC3;
#if defined (SDL_APP_DCC_UART_PRINTS_ENABLED)
					UART_printf("\n Reference clock source: HFOSC0 \n");
					UART_printf(" Test clock source: MAIN_SYSCLK0 \n");
#endif
			break;
					
			case 1:
					srcClk = SDL_DCC_CLK0_SRC_CLOCK0_1;
					testClk = SDL_DCC_CLK1_SRC_CLOCKSRC7;
#if defined (SDL_APP_DCC_UART_PRINTS_ENABLED)
					UART_printf("\n Reference clock source: HFOSC1 \n");
					UART_printf(" Test clock source: SERDES\n");
#endif
			break;

			default:
				/* Do nothing */
				break;
		}
	
		/* Step 1 Determine the clock frequencies for the sources */
		retVal = SDL_DCCAppGetClkfreqKHz(APP_DCC_MODULE_INST,
                                   srcClk,
                                   testClk,
                                   &clk0Freq, &clk1Freq);
		
		if (SDL_PASS == retVal)
		{
			/* Step 2 Figure out the seed values for successful completion */
			/* Get clock ratio */
			SDL_DCCAppGetClkRatio(clk0Freq,
								clk1Freq,
								&refClkRatioNum,
								&testClkRatioNum);
		
			configParams.mode    = APP_DCC_MODE;
			configParams.clk0Src = srcClk;
			configParams.clk1Src = testClk;
			
			/* Get the seed values for given clock selections and allowed drift */
			SDL_DCCAppSetSeedVals(clk0Freq,
								clk1Freq,
								refClkRatioNum,
								testClkRatioNum,
								APP_DCC_TEST_CLOCK_SRC_1_DRIFT,
								&configParams);
		
			/* Step 3 Configure Interrupts for normal completion */
			SDL_DCCAppRegisterIsr(APP_DCC_MODULE_INST);
		
			/* Step 4 Configure DCC instance */
			retVal = SDL_DCC_configure(SDL_DCC_INST_DCC0, &configParams);
			
			if (SDL_PASS == retVal)
			{
				retVal = SDL_DCC_verifyConfig(SDL_DCC_INST_DCC0, &configParams);
			}
			else
			{
				retVal = SDL_EFAIL;
			}
			
			if (SDL_PASS == retVal)
			{
				/* Clear the interrupt flag */
				isrFlag = 0U;
		
				SDL_DCC_getStaticRegs(SDL_DCC_INST_MCU_DCC0, &staticRegs);
#if defined (SDL_APP_DCC_UART_PRINTS_ENABLED)
				SDL_DCCAppPrint(APP_DCC_STR ": DCC configured \n");
		
				SDL_DCCAppPrint(APP_DCC_STR ": Enabling DCC\n");
#endif
				
				/* Step 5 Enable DCC */
				SDL_DCC_enable(SDL_DCC_INST_DCC0);
		
#if defined (SDL_APP_DCC_UART_PRINTS_ENABLED)
				SDL_DCCAppPrint(APP_DCC_STR ": Waiting for some time to perform Continuous mode \n");
#endif
				
				volatile int32_t i = 0;
				/* Wait for the ESM interrupt to report the error */
				do {
					i++;
					if (i > 0x0FFFFFFF)
					{
						/* Timeout for the wait */
						break;
					}
				} while (isrFlag == DCC_NO_INTERRUPT);
				
				if (isrFlag == DCC_INTERRUPT)
				{
#if defined (SDL_APP_DCC_UART_PRINTS_ENABLED)
					SDL_DCCAppPrint(APP_DCC_STR ": DCC Generated Error interrupt when it is not expected \n");
#endif
					isrFlag = DCC_NO_INTERRUPT;
					retVal = SDL_EFAIL;
				}
				else
				{
#if defined (SDL_APP_DCC_UART_PRINTS_ENABLED)
					SDL_DCCAppPrint(APP_DCC_STR ": DCC module is disabled \n");
#endif
					/* Step 6 Disable DCC */
					SDL_DCC_disable(SDL_DCC_INST_DCC0);
					retVal = SDL_PASS;
				}
			}
		}
	}
#if defined (SDL_APP_DCC_UART_PRINTS_ENABLED)
	UART_printf("\n\n\n Reference clock source: HFOSC1 \n");
	UART_printf(" Test clock source: MCAN \n");
#endif
	
	
    retVal = SDL_DCCAppGetClkfreqKHz(APP_DCC_MODULE_INST,
                                     APP_DCC_REF_CLOCK_SRC_0,
                                     APP_DCC_TEST_CLOCK_SRC_1,
                                     &clk0Freq, &clk1Freq);
	
    /* Step 7. Change reference clock source to higher frequency source 
               OR change the clock ratios to introduce the error
     */
    if (retVal == SDL_PASS)
    {	
        /* Step 8. Configure DCC and Enable */
        /* Change reference clock source to higher frequency source */
#if defined (SDL_APP_DCC_UART_PRINTS_ENABLED)
        SDL_DCCAppPrint(APP_DCC_STR ": Changing the clock source \n");
#endif
        configParams.clk1Src = APP_DCC_TEST_CLOCK_SRC_1_HIGHER;

        retVal = SDL_DCC_configure(SDL_DCC_INST_DCC0, &configParams);
#if defined (SDL_APP_DCC_UART_PRINTS_ENABLED)
        SDL_DCCAppPrint(APP_DCC_STR ": Enabling DCC and waiting for "
                        "Error interrupt \n");
#endif
	    /* Enable ERROR interrupt */
	    SDL_DCC_enableIntr(SDL_DCC_INST_DCC0, SDL_DCC_INTERRUPT_ERR);
        SDL_DCC_enable(SDL_DCC_INST_DCC0);

        /* Step 9. Wait for error notification */
			
		volatile int32_t i = 0;
		/* Wait for the ESM interrupt to report the error */
		do {
			i++;
			if (i > 0x0FFFFFFF)
			{
				/* Timeout for the wait */
				break;
			}
		} while (isrFlag == DCC_NO_INTERRUPT);
			
		if (isrFlag == DCC_INTERRUPT)
		{
#if defined (SDL_APP_DCC_UART_PRINTS_ENABLED)
			SDL_DCCAppPrint(APP_DCC_STR ": DCC Generated Error interrupt \n");
			SDL_DCCAppPrint(APP_DCC_STR ": Indicating clock drift/change \n");
#endif
			isrFlag = DCC_NO_INTERRUPT;
			/* Disabling the DCC */
			SDL_DCC_disable(SDL_DCC_INST_DCC0);
		}	
		else
		{
#if defined (SDL_APP_DCC_UART_PRINTS_ENABLED)
			SDL_DCCAppPrint(APP_DCC_STR ": Could not generate Error interrupt \n");
#endif
			retVal = SDL_EFAIL;
		}
    }
    
    else
    {
#if defined (SDL_APP_DCC_UART_PRINTS_ENABLED)
        SDL_DCCAppPrint(APP_DCC_STR ": Error : Could not derive clock "
                        "frequency!!!\r\n");
#endif
    }

    return retVal;
}
