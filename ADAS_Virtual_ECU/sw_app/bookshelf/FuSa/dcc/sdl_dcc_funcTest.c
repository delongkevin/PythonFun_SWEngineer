int32_t dcc_init_inst(uint32_t dccInst)
{
  /* DD-ID:{F9CA295C-BF65-4ba7-882F-C9D284D69327}*/
    SDL_DCC_config configParams;
	SDL_DCC_staticRegs staticRegs;
    int32_t retVal;

    configParams.clk0Src = SDL_APP_DCC_cfg[dccInst].srcClk;
    configParams.clk1Src = SDL_APP_DCC_cfg[dccInst].testClk;
    configParams.mode    = SDL_APP_DCC_cfg[dccInst].mode;

    /* Calculate valid, count0 and count1 */ 
    SDL_DCCCalculateSeeds(SDL_APP_DCC_cfg[dccInst].srcFreq, SDL_APP_DCC_cfg[dccInst].testFreq, 
        &configParams.clk0Seed, &configParams.clk1Seed, &configParams.clk0ValidSeed);


	retVal = SDL_DCC_configure(SDL_APP_DCC_cfg[dccInst].instance, &configParams);
	
	if (SDL_PASS == retVal)
	{
		retVal = SDL_DCC_verifyConfig(SDL_APP_DCC_cfg[dccInst].instance, &configParams);
	}
	else
	{
		retVal = SDL_EFAIL;
	}
	
	if (SDL_PASS == retVal)
	{
#if defined (SDL_APP_DCC_UART_PRINTS_ENABLED)
		SDL_DCCAppPrint(APP_DCC_STR ": DCC configured \n");
		SDL_DCCAppPrint(APP_DCC_STR ": Enabling DCC\n");
#endif
		(void)SDL_DCC_getStaticRegs(SDL_APP_DCC_cfg[dccInst].instance, &staticRegs);
		(void)SDL_DCC_enableIntr(SDL_APP_DCC_cfg[dccInst].instance, SDL_DCC_INTERRUPT_ERR);
		(void)SDL_DCC_enable(SDL_APP_DCC_cfg[dccInst].instance);
	    (void)memcpy((SDL_DCC_staticRegs *)&(DCC_staticRegs[SDL_APP_DCC_cfg[dccInst].instance]), (SDL_DCC_staticRegs *)&(staticRegs), sizeof(staticRegs));
    }


    return retVal;
}

