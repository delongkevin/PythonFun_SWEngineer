uint8_t crc::calculateCRC8(const uint8_t* crc_DataPtr, uint32_t crc_Length, uint8_t crc_StartValue8)
{
  uint32_t i;
  uint8_t crc = crc_StartValue8;
  if(NULL != crc_DataPtr)
  {
#if CRC_8_MODE == CRC_8_RUNTIME
    uint8_t cb, j;
    for(i=0; i<crc_Length; i++)
    {
      cb = crc_DataPtr[i];
      for(j=0; j<8; j++)
      {
         if((crc & 0x80) != (cb & 0x80))
         {
            crc = (crc << 1) ^ CRC_8_POLY;
         }
         else
         {
            crc <<= 1;
         }
         cb <<= 1;
      }
    }
#endif
#if CRC_8_MODE == CRC_8_TABLE
    for(i=0u; i<crc_Length; i++)
    {
      crc = crc_CRC8_table[crc ^ crc_DataPtr[i]];
    }
#endif
#if CRC_8_MODE == CRC_8_HARDWARE
#error "Hardware CRC not supported."
#endif
  /*lint -e{835} (A zero has been given as right argument to operator '^')
  * This is intended.
  */
  }
  else{/*do nothing*/}


  return crc ^ CRC_8_XOR_VAL;  /* PRQA S 2985 */ /* (A zero has been given as right argument to operator '^') This is intended.*/
}

