#ifndef __CANGW_API_H
#define __CANGW_API_H

#ifndef __KERNEL__

/**> Primitive typedefs from kernel */
typedef uint32_t    __u32;
typedef uint8_t     __u8;

/*******************************************************************************
 * Linux structures taken from KERNELSRC/include/uapi/linux/can.h
 ******************************************************************************/

/* special address description flags for the CAN_ID */
#define CAN_EFF_FLAG 0x80000000U /* EFF/SFF is set in the MSB */
#define CAN_RTR_FLAG 0x40000000U /* remote transmission request */
#define CAN_ERR_FLAG 0x20000000U /* error message frame */

/* valid bits in CAN ID for frame formats */
#define CAN_SFF_MASK 0x000007FFU /* standard frame format (SFF) */
#define CAN_EFF_MASK 0x1FFFFFFFU /* extended frame format (EFF) */
#define CAN_ERR_MASK 0x1FFFFFFFU /* omit EFF, RTR, ERR flags */

/*
 * Controller Area Network Identifier structure
 *
 * bit 0-28	: CAN identifier (11/29 bit)
 * bit 29	: error message frame flag (0 = data frame, 1 = error message)
 * bit 30	: remote transmission request flag (1 = rtr frame)
 * bit 31	: frame format flag (0 = standard 11 bit, 1 = extended 29 bit)
 */
typedef __u32 canid_t;

#define CAN_SFF_ID_BITS		11
#define CAN_EFF_ID_BITS		29

/*
 * Controller Area Network Error Message Frame Mask structure
 *
 * bit 0-28	: error class mask (see include/uapi/linux/can/error.h)
 * bit 29-31	: set to zero
 */
typedef __u32 can_err_mask_t;

/* CAN payload length and DLC definitions according to ISO 11898-1 */
#define CAN_MAX_DLC 8
#define CAN_MAX_DLEN 8

/* CAN FD payload length and DLC definitions according to ISO 11898-7 */
#define CANFD_MAX_DLC 15
#define CANFD_MAX_DLEN 64

/**
 * struct can_frame - basic CAN frame structure
 * \can_id:  CAN ID of the frame and CAN_*_FLAG flags, see canid_t definition
 * \can_dlc: frame payload length in byte (0 .. 8) aka data length code
 *           N.B. the DLC field from ISO 11898-1 Chapter 8.4.2.3 has a 1:1
 *           mapping of the 'data length code' to the real payload length
 * \__pad:   padding
 * \__res0:  reserved / padding
 * \__res1:  reserved / padding
 * \data:    CAN frame payload (up to 8 byte)
 */
struct can_frame {
	canid_t can_id;  /* 32 bit CAN_ID + EFF/RTR/ERR flags */
	__u8    can_dlc; /* frame payload length in byte (0 .. CAN_MAX_DLEN) */
	__u8    __pad;   /* padding */
	__u8    __res0;  /* reserved / padding */
	__u8    __res1;  /* reserved / padding */
	__u8    data[CAN_MAX_DLEN] __attribute__((aligned(8)));
};

/*
 * defined bits for canfd_frame.flags
 *
 * The use of struct canfd_frame implies the Extended Data Length (EDL) bit to
 * be set in the CAN frame bitstream on the wire. The EDL bit switch turns
 * the CAN controllers bitstream processor into the CAN FD mode which creates
 * two new options within the CAN FD frame specification:
 *
 * Bit Rate Switch - to indicate a second bitrate is/was used for the payload
 * Error State Indicator - represents the error state of the transmitting node
 *
 * As the CANFD_ESI bit is internally generated by the transmitting CAN
 * controller only the CANFD_BRS bit is relevant for real CAN controllers when
 * building a CAN FD frame for transmission. Setting the CANFD_ESI bit can make
 * sense for virtual CAN interfaces to test applications with echoed frames.
 */
#define CANFD_BRS 0x01 /* bit rate switch (second bitrate for payload data) */
#define CANFD_ESI 0x02 /* error state indicator of the transmitting node */

/**
 * struct canfd_frame - CAN flexible data rate frame structure
 * \can_id: CAN ID of the frame and CAN_*_FLAG flags, see canid_t definition
 * \len:    frame payload length in byte (0 .. CANFD_MAX_DLEN)
 * \flags:  additional flags for CAN FD
 * \__res0: reserved / padding
 * \__res1: reserved / padding
 * \data:   CAN FD frame payload (up to CANFD_MAX_DLEN byte)
 */
struct canfd_frame {
	canid_t can_id;  /* 32 bit CAN_ID + EFF/RTR/ERR flags */
	__u8    len;     /* frame payload length in byte */
	__u8    flags;   /* additional flags for CAN FD */
	__u8    __res0;  /* reserved / padding */
	__u8    __res1;  /* reserved / padding */
	__u8    data[CANFD_MAX_DLEN] __attribute__((aligned(8)));
};

/*******************************************************************************
 * Linux structures taken from KERNELSRC/include/uapi/linux/can/netlink.h
 ******************************************************************************/

/*
 * CAN bit-timing parameters
 *
 * For further information, please read chapter "8 BIT TIMING
 * REQUIREMENTS" of the "Bosch CAN Specification version 2.0"
 * at http://www.semiconductors.bosch.de/pdf/can2spec.pdf.
 */
struct can_bittiming {
	__u32 bitrate;		/* Bit-rate in bits/second */
	__u32 sample_point;	/* Sample point in one-tenth of a percent */
	__u32 tq;		/* Time quanta (TQ) in nanoseconds */
	__u32 prop_seg;		/* Propagation segment in TQs */
	__u32 phase_seg1;	/* Phase buffer segment 1 in TQs */
	__u32 phase_seg2;	/* Phase buffer segment 2 in TQs */
	__u32 sjw;		/* Synchronisation jump width in TQs */
	__u32 brp;		/* Bit-rate prescaler */
};

#endif  /* __KERNEL__ */

/*******************************************************************************
 * Common types definitions
 ******************************************************************************/

typedef struct can_frame        can_frame_t;
typedef struct canfd_frame      canfd_frame_t;
typedef struct can_bittiming    can_bittiming_t;

/**> CAN frame FD flag */
#define CANGW_FDF 0x04

#ifndef offsetof
#define offsetof(type, field)   (size_t)(uintptr_t)(&(((type) *)(uintptr_t)0)->(field))
#endif

/**
 * \brief   Can frame length calculator
 */
static inline size_t cangw_cf_size(canfd_frame_t *cf)
{
    return offsetof(canfd_frame_t, data) + cf->len;
}

/**
 * \brief   Generic input RPMessage
 */
typedef struct cangw_rpmsg_base
{
    /**> Message type */
    uint8_t                 type;

    /**> RX/TX FIFO states */
    uint8_t                 flags;

    /**> Transmission queue tail (last transmitted packet, incremented on tx-isr) */
    uint8_t                 tx_tail;

    /**> Transmission queue head (last submitted packet, incremented on xmit) */
    uint8_t                 tx_head;

    /**> Rreceiving timestamp (in CAN clock ticks) */
    uint64_t                tstamp;

}   __attribute__((packed)) cangw_rpmsg_base_t;

/**
 * \brief   Control RPMessage
 */
typedef struct cangw_rpmsg_ctrl
{
    /**> Generic RPMessage data */
    cangw_rpmsg_base_t      base;

    /**> Operation code */
    uint32_t                opcode;

    /**> Nominal bitrate */
    struct can_bittiming    nbrt;

    /**> Data bitrate */
    struct can_bittiming    dbrt;

}   __attribute__((packed)) cangw_rpmsg_ctrl_t;

/**
 * \brief   Input RPMessage
 */
typedef struct cangw_rpmsg_txpkt
{
    /**> Generic RPMessage data */
    cangw_rpmsg_base_t      base;
    
    /**> CAN-TX message (maybe a union can/canfd? - tbd) */
    struct canfd_frame      cf;

}   __attribute__((packed)) cangw_rpmsg_txpkt_t;

/**
 * \brief   Output RPMessage
 */
typedef struct cangw_rpmsg_rxpkt
{
    /**> Generic RPMessage data */
    cangw_rpmsg_base_t      base;
    
    /**> CAN-RX message */
    struct canfd_frame      cf;

}   __attribute__((packed)) cangw_rpmsg_rxpkt_t;

/**
 * \brief   Generic RPMessage
 */
typedef union cangw_rpmsg
{
    /**> Generic header */
    cangw_rpmsg_base_t      base;

    /**> Control message */
    cangw_rpmsg_ctrl_t      ctrl;

    /**> CAN-TX message */
    cangw_rpmsg_txpkt_t     tx;

    /**> CAN-RX message */
    cangw_rpmsg_rxpkt_t     rx;

}   __attribute__((packed)) cangw_rpmsg_t;

/*******************************************************************************
 * RPMSG message types
 ******************************************************************************/

/**> CAN RX-FIFO length */
#define CANGW_RX_FIFO_WIDTH         64U

/**> CAN TX-FIFO length */
#define CANGW_TX_FIFO_WIDTH         32U

/**> IPC messages types */
#define CANGW_RPMSG_CTRL            1
#define CANGW_RPMSG_RXPKT           2
#define CANGW_RPMSG_TXPKT           3
#define CANGW_RPMSG_TXSTATUS        4

/**> Message flags */
#define CANGW_FLAG_TX_FIFO_FULL     (1 << 0)
#define CANGW_FLAG_RX_FIFO_FULL     (1 << 1)

/**> Control message opcodes */
#define CANGW_CTRL_START            1
#define CANGW_CTRL_STOP             2

/**> Endpoints assignments */
#define CANGW_EP_MCU_MCAN0          10
#define CANGW_EP_MCU_MCAN1          11
#define CANGW_EP_MAIN_MCAN0         20
#define CANGW_EP_MAIN_MCAN1         21
#define CANGW_EP_MAIN_MCAN2         22

#endif  /* __CANGW_API_H */
